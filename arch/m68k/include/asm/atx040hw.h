#ifndef _M68K_ATX030_HW_H
#define _M68K_ATX030_HW_H

#include <asm/bootinfo-atx040.h>

#define ATX040_UART_BASE	0xF1000000

#define ATX040_SPI0_BASE	0xF2000000
#define ATX040_SPI1_BASE	0xF4000000
#define ATX040_SPI2_BASE	0xF5000000

#define ATX040_IRQC_BASE	0xF6000000

#define ATX040_I2S_BASE		0xF7000000

#define ATX040_VGA_BASE		0xF8000000

#define ATX040_UART_IRQ		(IRQ_USER + 0)
#define ATX040_SYSTICK_IRQ	(IRQ_USER + 1)
#define ATX040_SPI_ETH_IRQ	(IRQ_USER + 2)
#define ATX040_I2S_SND_IRQ	(IRQ_USER + 3)

#endif
