#ifndef _M68K_ATX030_HW_H
#define _M68K_ATX030_HW_H

#include <asm/bootinfo-atx040.h>

#define ATX040_WB_UART_BASE	0xC0000000
#define ATX040_WB_UART_DATA	0xC0000000
#define ATX040_WB_UART_STATUS	0xC0000004

#define ATX040_SPI0_BASE	0xC4000000
#define ATX040_SPI1_BASE	0xCC000000
#define ATX040_SPI2_BASE	0xD0000000

#define ATX040_IRQC_BASE	0xD4000000

#define ATX040_I2S_BASE		0xD8000000

#define ATX040_WB_UART_IRQ	(IRQ_USER + 0)
#define ATX040_SYSTICK_IRQ	(IRQ_USER + 1)
#define ATX040_SPI_ETH_IRQ	(IRQ_USER + 2)
#define ATX040_I2S_SND_IRQ	(IRQ_USER + 3)

#endif
